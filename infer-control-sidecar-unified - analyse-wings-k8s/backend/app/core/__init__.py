#
# 本包是 sidecar 控制平面的核心，负责从用户参数到引擎启动脚本的完整转换链路：
#
#   用户 CLI/环境变量
#     ↓  start_args_compat.py  (解析并标准化启动参数)
#     ↓  config_loader.py      (加载默认配置 → 合并用户配置 → 自动选引擎)
#     ↓  hardware_detect.py    (探测硬件类型: nvidia/ascend)
#     ↓  port_plan.py          (端口规划: backend/proxy/health)
#     ↓  wings_entry.py        (生成 LauncherPlan，桥接参数与引擎适配器)
#     ↓  engine_manager.py     (动态加载引擎适配器，调用 build_start_script)
#     ↓
#   输出: start_command.sh（写入共享卷，供引擎容器读取执行）
#
# Sidecar 契约:
#   - 保持导入关系简洁，避免循环依赖
#   - 核心模块不应包含隐式初始化逻辑
